<template>
  <div>
    <select 
      class="select"
      v-model="modelValue"
      @change="changeOption"
      >
      <option value="" disabled>Choose from the list</option>
      <option 
        v-for="option in options"
        :key="option.value" 
        :value="option.value"
        >
        {{ option.name }}
      </option>
    </select>
  </div>
</template>

<script>
export default {
  name:'my-select',
  props: {
    modelValue: {
      type: String
    },
    options: {
      type: Array,
      default: () => []
    }
  },
  methods: {
    changeOption(event) {
      this.$emit('update:modelValue', event.target.value)
    }
  }
}
</script>

<style scoped>
.select {
  width: 300px;
  height: 40px;
  border: 1px solid teal;
  padding: 5px;
}
</style>